    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // TODO add your handling code here:
        try {
            Object[] Options = {"Cambiar contrase√±a", "Desactivar cuenta"};
            int Ans = JOptionPane.showOptionDialog(this, "Que accion desea realizar?", "Mi cuenta", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, null, Options, 0);

            if (Ans == 0) {

                if (Usuarios.ChangePass()) {
                    JOptionPane.showMessageDialog(this, "Se ha cambiado la contrase√±a exitosamente", "Cambiar contrase√±a", JOptionPane.INFORMATION_MESSAGE);
                } else JOptionPane.showMessageDialog(this, "No se ha podido cambiar la contrase√±a", "Cambiar contrase√±a", JOptionPane.INFORMATION_MESSAGE);

            } else {

                if (Usuarios.DeactivateAccount()) {
                    JOptionPane.showMessageDialog(this, "Se ha desactivado la cuenta exitosamente", "Cuenta desactivada", JOptionPane.INFORMATION_MESSAGE);
                    menuInicio l = new menuInicio();
                    l.setVisible(true);
                    dispose();
                }

            } 

        } catch (Exception e){}
    }//GEN-LAST:event_jToggleButton2ActionPerformed

