    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // TODO add your handling code here:
       String name = Ingresouser.getText();
        String pass = ingresopass.getText();
    
    if (name.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos");
        return;
    } 
    
    boolean userCreated = Usuarios.addUser(name, pass);
    if (userCreated) {
        Usuarios.setPlayer1(name, pass);
        MenuPrincipal men = new MenuPrincipal();
        men.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "No se ha podido registrar la cuenta, intente cambiando el nombre de usuario\ny que su contrase√±a cuente con unicamente 5 caracteres", "ERROR", JOptionPane.WARNING_MESSAGE);
    }
        
        
               
    }//GEN-LAST:event_GuardarActionPerformed

